    public void openChoi() throws SQLException{
        LinhVuc linhVuc= new LinhVuc();
        if (cbLinhVuc.getSelectedItem().toString()=="All") {
            linhVuc=null;
        }else{
        linhVuc=LinhVucDao.findMaLinhVuc(cbLinhVuc.getSelectedItem().toString());
        }
        JFrameChoi form = new JFrameChoi(linhVuc);
        DesktopPane.add(form);
        form.setVisible(true);
    }
