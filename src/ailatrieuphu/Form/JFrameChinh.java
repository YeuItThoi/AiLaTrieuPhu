    public void loadLinhVuc(){
           DefaultComboBoxModel model = (DefaultComboBoxModel) cbLinhVuc.getModel(); 
           model.removeAllElements();
           model.addElement("All");
           try{
               List<LinhVuc> list = LinhVucDao.select();
               for(LinhVuc lv:list){
                   model.addElement(lv.getTenLinhVuc());
               }
           }
           catch(Exception e){
               DialogHelper.alert(this, "Lỗi truy vấn dữ liệu");
           }
    }

    public void statusLogin(){
        if(ShareHelper.USER!=null){
            if (ShareHelper.USER.getAdmin()) {
                btnAdmin.setVisible(true);
            }else{
                btnAdmin.setVisible(false);
            }
            btnDangNhap.setText("Đăng Xuất");
            lblDiem.setText(ShareHelper.USER.getDiem()+"");
            lblTen.setText(ShareHelper.USER.getHoTen());
        }else{
            btnAdmin.setVisible(false);
            btnDangNhap.setText("Đăng Nhập");
            lblDiem.setText("");
            lblTen.setText("");
        }
    }
    
    public boolean checkLogin(){
        if(ShareHelper.USER!=null){
            return true;
        }
        return false;
    }
